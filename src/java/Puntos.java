public class Puntos {
    private int X = 0, Y = 0;

    public Puntos() {
    }

    public Puntos(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }

    
    public int getX() {
        return X;
    }

    public void setX(int X) {
        this.X = X;
    }

    public int getY() {
        return Y;
    }

    public void setY(int Y) {
        this.Y = Y;
    }
}
